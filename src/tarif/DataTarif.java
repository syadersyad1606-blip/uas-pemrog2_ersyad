    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:

        if(txtNamaTarif.getText().isEmpty() || txtHarga.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Nama Tarif dan Harga wajib diisi!");
            return;
        }

        try{

            double harga = Double.parseDouble(txtHarga.getText());

            String sql = "INSERT INTO tb_tarif(nama_tarif,harga_per_jam,keterangan) VALUES(?,?,?)";

            pst = conn.prepareStatement(sql);

            pst.setString(1, txtNamaTarif.getText());
            pst.setDouble(2, harga);
            pst.setString(3, txtKeterangan.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");

            tampilData();

            resetForm();

        }catch(NumberFormatException nfe){

            JOptionPane.showMessageDialog(null,"Harga harus berupa angka!");

        }catch(Exception e){

            JOptionPane.showMessageDialog(null,e);

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:

        if(txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Pilih data pada tabel terlebih dahulu!");
            return;
        }

        try{

            double harga = Double.parseDouble(txtHarga.getText());

            String sql = "UPDATE tb_tarif SET nama_tarif=?, harga_per_jam=?, keterangan=? WHERE id_tarif=?";

            pst = conn.prepareStatement(sql);

            pst.setString(1, txtNamaTarif.getText());
            pst.setDouble(2, harga);
            pst.setString(3, txtKeterangan.getText());
            pst.setString(4, txtId.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null,"Data Berhasil Diupdate");

            tampilData();

            resetForm();

        }catch(NumberFormatException nfe){

            JOptionPane.showMessageDialog(null,"Harga harus berupa angka!");

        }catch(Exception e){

            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:

        if(txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Pilih data pada tabel terlebih dahulu!");
            return;
        }

        int jawab = JOptionPane.showConfirmDialog(null,"Yakin hapus data ini?","Konfirmasi",JOptionPane.YES_NO_OPTION);

        if(jawab != JOptionPane.YES_OPTION){
            return;
        }

        try{

            String sql = "DELETE FROM tb_tarif WHERE id_tarif=?";

            pst = conn.prepareStatement(sql);

            pst.setString(1, txtId.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null,"Data Berhasil Dihapus");

            tampilData();

            resetForm();

        }catch(Exception e){

            JOptionPane.showMessageDialog(null,e);

        }
    }//GEN-LAST:event_btnHapusActionPerformed

