    private void tblTarifMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTarifMouseClicked
        // TODO add your handling code here:
        int baris = tblTarif.getSelectedRow();

        if(baris < 0){
            return;
        }

        txtId.setText(tblTarif.getValueAt(baris,0).toString());
        txtNamaTarif.setText(tblTarif.getValueAt(baris,1).toString());
        txtHarga.setText(tblTarif.getValueAt(baris,2).toString());
        txtKeterangan.setText(tblTarif.getValueAt(baris,3) == null ? "" : tblTarif.getValueAt(baris,3).toString());
    }//GEN-LAST:event_tblTarifMouseClicked

