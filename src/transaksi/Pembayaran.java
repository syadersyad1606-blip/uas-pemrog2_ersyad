    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:

        try{

            double uangBayar = Double.parseDouble(txtUangBayar.getText());

            if(uangBayar < totalBayar){
                JOptionPane.showMessageDialog(null,"Uang bayar kurang dari total tagihan!");
                txtKembalian.setText("");
                return;
            }

            double kembalian = uangBayar - totalBayar;

            txtKembalian.setText(String.valueOf(kembalian));

        }catch(NumberFormatException nfe){

            JOptionPane.showMessageDialog(null,"Uang Bayar harus berupa angka!");

        }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        // TODO add your handling code here:

        if(txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Data transaksi tidak valid!");
            return;
        }

        if(txtKembalian.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Klik tombol Hitung terlebih dahulu!");
            return;
        }

        try{

            double uangBayar = Double.parseDouble(txtUangBayar.getText());
            double kembalian = Double.parseDouble(txtKembalian.getText());

            String sql = "UPDATE tb_transaksi SET uang_bayar=?, kembalian=?, status_bayar='Lunas' WHERE id_transaksi=?";

            pst = conn.prepareStatement(sql);
            pst.setDouble(1, uangBayar);
            pst.setDouble(2, kembalian);
            pst.setString(3, txtId.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null,"Pembayaran Berhasil Diproses");

            dispose();

        }catch(Exception e){

            JOptionPane.showMessageDialog(null,e);

        }
    }//GEN-LAST:event_btnProsesActionPerformed

