    private void muatData(){

        try{

            String sql = "SELECT id_transaksi, total_bayar FROM tb_transaksi WHERE id_transaksi=?";

            pst = conn.prepareStatement(sql);
            pst.setString(1, idTransaksi);

            rs = pst.executeQuery();

            if(rs.next()){

                txtId.setText(rs.getString("id_transaksi"));
                totalBayar = rs.getDouble("total_bayar");
                txtTotalBayar.setText(String.valueOf(totalBayar));

            }else{

                JOptionPane.showMessageDialog(null,"Data transaksi tidak ditemukan!");

            }

        }catch(Exception e){

            JOptionPane.showMessageDialog(null,e);

        }
    }

